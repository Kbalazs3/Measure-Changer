

def hour_to_minute(hour):
    return int(hour) * 60


def minute_to_hour(minute):
    return int(minute) / 60


def minute_to_sec(minute):
    return int(minute) * 60


def sec_to_minute(sec):
    return int(sec) / 60


def day_to_hour(day):
    return int(day) * 24


def hour_to_day(hour):
    return int(hour) / 24

